package cn.retech.domainbean_model.book_categories;

import java.util.List;

public final class BookCategoriesNetRespondBean {
	private final List<BookCategory> categories;

	public BookCategoriesNetRespondBean(List<BookCategory> categories) {
		this.categories = categories;
	}

	@Override
	public String toString() {
		return "BookCategoriesNetRespondBean [categories=" + categories + "]";
	}

	public List<BookCategory> getCategories() {
		return categories;
	}

}
